//    endpoints.MapHub<OrderHub>("/orders");
//});
app.MapHub<OrderHub>("/orders");
app.UseSwagger();
app.UseSwaggerUI();
app.UseHttpsRedirection();
app.UseRateLimiter();
app.UseAuthorization();
app.MapControllers();

app.Run();
